"""CALCULATOR"""

def addition(int1, int2):
    return int1 + int2

def subtraction(int1, int2):
    return int1 - int2

def multiplication(int1, int2):
    return int1 * int2

def division(int1, int2):
    return int1 / int2

def modulo(int1, int2):
    return int1 % int2


print(addition(2, 3))
print(subtraction(2, 3))
print(multiplication(2, 3))
print(division(2, 3))
print(modulo(2, 3))

